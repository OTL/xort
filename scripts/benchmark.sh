#!/usr/bin/env bash
# Reproducible benchmark: xort vs GNU sort, via hyperfine.
#
# Fairness notes:
#   * Both tools run under LC_ALL=C (byte ordering). This is the *conservative*
#     comparison — GNU sort is dramatically slower in a UTF-8 locale, and we
#     deliberately do NOT exploit that here.
#   * Both tools use all cores by default (GNU coreutils >= 8 auto-parallelizes).
#   * Inputs are regular files; a warmup run primes the page cache so we measure
#     CPU/algorithm, not cold disk.
set -euo pipefail

XORT="${XORT:-./target/release/xort}"
OUT="${OUT:-benchmarks/results.md}"
DATA="${DATA:-$(mktemp -d)}"
export LC_ALL=C
mkdir -p "$(dirname "$OUT")"

echo "Generating datasets in $DATA ..."
# 10M random integers (~85 MB)
awk 'BEGIN{srand(1);for(i=0;i<10000000;i++)print int(rand()*100000000)}' > "$DATA/ints.txt"
# 10M random decimals
awk 'BEGIN{srand(2);for(i=0;i<10000000;i++)printf "%.4f\n", rand()*1000000-500000}' > "$DATA/floats.txt"
# 8M short text lines drawn from a modest vocabulary (many duplicates)
awk 'BEGIN{srand(3);for(i=0;i<8000000;i++){printf "%c%c%c%c%c%c\n",
  97+int(rand()*26),97+int(rand()*26),97+int(rand()*26),
  97+int(rand()*26),97+int(rand()*26),97+int(rand()*26)}}' > "$DATA/words.txt"
ls -lh "$DATA"

# --- correctness gate: identical output before we trust any timing ----------
echo "Verifying output parity ..."
check() { # desc, xort-args..., -- , gnu-args...
  local desc="$1"; shift
  local f=(); local g=()
  while [ "$1" != "--" ]; do f+=("$1"); shift; done; shift
  while [ "$#" -gt 0 ]; do g+=("$1"); shift; done
  if cmp -s <("$XORT" "${f[@]}") <(sort "${g[@]}"); then
    echo "  ok: $desc"
  else
    echo "  MISMATCH: $desc"; exit 1
  fi
}
check "numeric"       -n "$DATA/ints.txt"   -- -n "$DATA/ints.txt"
check "floats"        -n "$DATA/floats.txt" -- -n "$DATA/floats.txt"
check "text"          "$DATA/words.txt"     --    "$DATA/words.txt"
check "unique text"   -u "$DATA/words.txt"  -- -u "$DATA/words.txt"
# top-N parity is against `sort | head`
cmp -s <("$XORT" -n --top 100 "$DATA/ints.txt") <(sort -n "$DATA/ints.txt" | head -100) \
  && echo "  ok: top-100" || { echo "  MISMATCH: top-100"; exit 1; }

# --- timings ----------------------------------------------------------------
RUNS="${RUNS:-5}"; WARMUP="${WARMUP:-1}"
hb() { hyperfine --warmup "$WARMUP" --runs "$RUNS" --export-markdown "$1" "${@:2}"; }

echo "Benchmarking (this takes a few minutes) ..."
hb "$DATA/r_num.md"  -n "GNU numeric"       "LC_ALL=C sort -n $DATA/ints.txt > /dev/null" \
                        -n "xort numeric"     "$XORT -n $DATA/ints.txt > /dev/null"
hb "$DATA/r_flt.md"  -n "GNU float"         "LC_ALL=C sort -n $DATA/floats.txt > /dev/null" \
                        -n "xort float"       "$XORT -n $DATA/floats.txt > /dev/null"
hb "$DATA/r_txt.md"  -n "GNU text"          "LC_ALL=C sort $DATA/words.txt > /dev/null" \
                        -n "xort text"        "$XORT $DATA/words.txt > /dev/null"
hb "$DATA/r_uniq.md" -n "GNU unique"        "LC_ALL=C sort -u $DATA/words.txt > /dev/null" \
                        -n "xort unique"      "$XORT -u $DATA/words.txt > /dev/null"
hb "$DATA/r_top.md"  -n "GNU sort|head 100" "LC_ALL=C sort -n $DATA/ints.txt | head -100 > /dev/null" \
                        -n "xort --top 100"   "$XORT -n --top 100 $DATA/ints.txt > /dev/null"

{
  echo "# xort vs GNU sort — benchmark results"
  echo
  echo "Generated by \`scripts/benchmark.sh\` with hyperfine $(hyperfine --version | awk '{print $2}'),"
  echo "GNU coreutils sort $(sort --version | head -1 | grep -oE '[0-9]+\.[0-9]+'), on $(nproc) cores."
  echo "Both tools run under \`LC_ALL=C\`; both use all cores. Output verified byte-identical."
  echo
  for pair in "Numeric (10M ints):r_num" "Float (10M decimals):r_flt" \
              "Text (8M lines):r_txt" "Unique text (8M lines):r_uniq" \
              "Top-100 (10M ints):r_top"; do
    title="${pair%%:*}"; file="${pair##*:}"
    echo "## $title"; echo
    cat "$DATA/$file.md"; echo
  done
} > "$OUT"

echo "Wrote $OUT"
